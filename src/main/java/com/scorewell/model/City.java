package com.scorewell.model;

public class City {

	private String cityName;
	private String cityPin;
	private long cityPopulatin;
	
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getCityPin() {
		return cityPin;
	}
	public void setCityPin(String cityPin) {
		this.cityPin = cityPin;
	}
	public long getCityPopulatin() {
		return cityPopulatin;
	}
	public void setCityPopulatin(long cityPopulatin) {
		this.cityPopulatin = cityPopulatin;
	}
	
	
}
